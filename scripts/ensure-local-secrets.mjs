#!/usr/bin/env node
/**
 * Ensure gitignored local secret files exist for Nuxt dev (.env) and Wrangler (.dev.vars).
 * Does not overwrite existing non-empty values. Generates secrets that pass runtime validation
 * (32+ random bytes, base64url — no placeholder fragments).
 */
import { randomBytes } from 'node:crypto'
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')

function generateSecret() {
  return randomBytes(32).toString('base64url')
}

function parseEnv(text) {
  /** @type {Record<string, string>} */
  const values = {}
  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const eq = trimmed.indexOf('=')
    if (eq <= 0) continue
    values[trimmed.slice(0, eq)] = trimmed.slice(eq + 1)
  }
  return values
}

/**
 * @param {string} relativePath
 */
function readEnvFile(relativePath) {
  const absolute = join(root, relativePath)
  if (!existsSync(absolute)) return {}
  return parseEnv(readFileSync(absolute, 'utf8'))
}

/**
 * Prefer an existing non-empty value from either local file; otherwise generate once.
 * @param {string} key
 * @param {Record<string, string>[]} sources
 * @param {() => string} fallback
 */
function pickOrGenerate(key, sources, fallback) {
  for (const source of sources) {
    const value = source[key]?.trim()
    if (value) return value
  }
  return fallback()
}

/**
 * @param {string} relativePath
 * @param {Record<string, string>} required
 * @param {string[]} headerLines
 */
function ensureFile(relativePath, required, headerLines) {
  const absolute = join(root, relativePath)
  const existed = existsSync(absolute)
  /** @type {Record<string, string>} */
  const current = existed ? parseEnv(readFileSync(absolute, 'utf8')) : {}
  let updated = false
  for (const [key, value] of Object.entries(required)) {
    if (!current[key]?.trim()) {
      current[key] = value
      updated = true
    }
  }
  if (existed && !updated) {
    return { path: relativePath, created: false, updated: false }
  }

  const body = [
    ...headerLines,
    ...Object.entries(current).map(([key, value]) => `${key}=${value}`),
    ''
  ].join('\n')
  writeFileSync(absolute, body, 'utf8')
  return { path: relativePath, created: !existed, updated: existed && updated }
}

const existingEnv = readEnvFile('.env')
const existingDevVars = readEnvFile('.dev.vars')
const sources = [existingEnv, existingDevVars]

const sessionSecret = pickOrGenerate('SESSION_SECRET', sources, generateSecret)
const authEncryptionKey = pickOrGenerate('AUTH_ENCRYPTION_KEY', sources, generateSecret)
const siteUrl = pickOrGenerate('NUXT_PUBLIC_SITE_URL', sources, () => 'http://localhost:3000')

const results = [
  ensureFile('.env', {
    NUXT_PUBLIC_SITE_URL: siteUrl,
    SESSION_SECRET: sessionSecret,
    AUTH_ENCRYPTION_KEY: authEncryptionKey
  }, [
    '# Local Nuxt secrets (gitignored). Generated by scripts/ensure-local-secrets.mjs.',
    '# Do not commit. Rotate anytime by deleting this file and re-running the script.'
  ]),
  ensureFile('.dev.vars', {
    SESSION_SECRET: sessionSecret,
    AUTH_ENCRYPTION_KEY: authEncryptionKey
  }, [
    '# Local Wrangler secrets (gitignored). Generated by scripts/ensure-local-secrets.mjs.',
    '# Used by `pnpm preview` / wrangler.local.jsonc. Do not commit.'
  ])
]

for (const result of results) {
  if (result.created) {
    console.log(`[ensure-local-secrets] created ${result.path}`)
  } else if (result.updated) {
    console.log(`[ensure-local-secrets] updated ${result.path}`)
  } else {
    console.log(`[ensure-local-secrets] kept existing ${result.path}`)
  }
}
