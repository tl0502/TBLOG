import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'

const root = resolve(import.meta.dirname, '..')
const migrationsDir = resolve(root, 'server/database/migrations')
const outPath = resolve(root, 'server/database/migrations-manifest.generated.ts')

const files = readdirSync(migrationsDir).filter((f) => f.endsWith('.sql')).sort()
const entries = files
  .map((name) => {
    const sql = readFileSync(resolve(migrationsDir, name), 'utf8')
    return `  { name: ${JSON.stringify(name)}, sql: ${JSON.stringify(sql)} }`
  })
  .join(',\n')

const output = `// AUTO-GENERATED by scripts/generate-migrations-manifest.mjs. Do not edit by hand.
// Regenerate after adding a migration: pnpm generate:migrations
import type { EmbeddedMigration } from '../repositories/contracts/migration-repository'

export const embeddedMigrations: ReadonlyArray<EmbeddedMigration> = [
${entries}
]
`
writeFileSync(outPath, output)
console.log(`[migrations-manifest] wrote ${files.length} migrations to server/database/migrations-manifest.generated.ts`)
